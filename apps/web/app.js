function applyLanguage(locale) {
  document.documentElement.lang = locale;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    const value = messages[locale][key];
    if (value) node.textContent = value;
  });
}

function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

function scoreResume(resumeText, jdText) {
  const resume = resumeText.toLowerCase();
  const jd = jdText.toLowerCase();
  const keywords = [
    "scale",
    "distributed",
    "reliability",
    "stakeholder",
    "mentor",
    "performance",
    "python",
    "system",
    "ownership",
    "api",
  ];
  let matches = 0;
  keywords.forEach((word) => {
    if (resume.includes(word) && jd.includes(word)) matches += 1;
  });

  const evidenceSignals = ["%", "improved", "reduced", "increased", "led", "designed", "built"];
  let evidenceScore = 0;
  evidenceSignals.forEach((token) => {
    if (resume.includes(token)) evidenceScore += 1;
  });

  const alignment = clamp(Math.round((matches / keywords.length) * 100) + 18, 18, 98);
  const evidence = clamp(Math.round((evidenceScore / evidenceSignals.length) * 100) + 12, 12, 97);
  return { alignment, evidence };
}

